public class PosNegZero {
    public static void main(String[] args) {
        int a = 20;

        if(a < 0){
            System.out.println(a + " is Negative");
        }
        if(a > 0){
            System.out.println(a + " is Positive");
        }
        if(a == 0){
            System.out.println("Equal");
        }


    }
}
